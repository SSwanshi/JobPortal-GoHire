#!/usr/bin/env node

const { spawnSync } = require("child_process");
const fs = require("fs");
const path = require("path");

const repoRoot = path.resolve(__dirname, "..", "..");
const reportsRoot = path.join(repoRoot, "test-reports");

const projects = [
  {
    name: "admin-backend",
    cwd: path.join(repoRoot, "goHire", "admin", "backend"),
  },
  {
    name: "applicant-backend",
    cwd: path.join(repoRoot, "goHire", "applicant", "backend"),
  },
  {
    name: "recruiter-backend",
    cwd: path.join(repoRoot, "goHire", "recruiter", "backend"),
  },
];

function ensureDirectory(directoryPath) {
  fs.mkdirSync(directoryPath, { recursive: true });
}

function resetProjectArtifacts(projectReportDir, coverageDirectory, resultsFile, commandLogFile) {
  ensureDirectory(projectReportDir);

  // Remove prior run artifacts so each execution writes fresh report files.
  fs.rmSync(resultsFile, { force: true });
  fs.rmSync(commandLogFile, { force: true });
  fs.rmSync(coverageDirectory, { recursive: true, force: true });

  ensureDirectory(coverageDirectory);
}

function runNpmTestCommand(cwd, args) {
  const attempts =
    process.platform === "win32"
      ? [
          { command: "npm", shell: true, label: "npm (shell)" },
          { command: "npm.cmd", shell: false, label: "npm.cmd" },
        ]
      : [{ command: "npm", shell: false, label: "npm" }];

  let lastAttempt = null;
  for (const attempt of attempts) {
    const result = spawnSync(attempt.command, args, {
      cwd,
      shell: attempt.shell,
      encoding: "utf8",
      stdio: "pipe",
    });

    lastAttempt = { attempt, result };
    const commandNotFound = result.error && result.error.code === "ENOENT";
    if (!commandNotFound) {
      return lastAttempt;
    }
  }

  return lastAttempt;
}

function writeFallbackResults(project, filePath, exitCode, stderr, runTimestamps) {
  const fallbackPayload = {
    success: false,
    project: project.name,
    error: "Jest results file was not generated by npm test.",
    exitCode,
    stderr,
    startedAt: runTimestamps.startedAt,
    endedAt: runTimestamps.endedAt,
  };

  fs.writeFileSync(filePath, JSON.stringify(fallbackPayload, null, 2), "utf8");
}

function runProjectTests(project) {
  const projectReportDir = path.join(reportsRoot, project.name);
  const coverageDirectory = path.join(projectReportDir, "coverage");
  const resultsFile = path.join(projectReportDir, "results.json");
  const commandLogFile = path.join(projectReportDir, "command.log");
  const coverageDirectoryArg = path.relative(project.cwd, coverageDirectory);
  const resultsFileArg = path.relative(project.cwd, resultsFile);

  resetProjectArtifacts(projectReportDir, coverageDirectory, resultsFile, commandLogFile);

  const args = [
    "test",
    "--",
    "--coverage",
    "--coverageReporters=text-summary",
    "--coverageReporters=lcov",
    `--coverageDirectory=${coverageDirectoryArg}`,
    "--json",
    `--outputFile=${resultsFileArg}`,
  ];

  const startedAt = new Date().toISOString();
  const commandResult = runNpmTestCommand(project.cwd, args);
  const attempt = commandResult ? commandResult.attempt : null;
  const result = commandResult ? commandResult.result : null;
  const endedAt = new Date().toISOString();

  const stdout = result && result.stdout ? result.stdout : "";
  const stderr = result && result.stderr ? result.stderr : "";
  if (stdout) {
    process.stdout.write(stdout);
  }
  if (stderr) {
    process.stderr.write(stderr);
  }

  const exitCode = result && typeof result.status === "number" ? result.status : 1;

  const runTimestamps = { startedAt, endedAt };
  const commandLog = [
    `project=${project.name}`,
    `cwd=${project.cwd}`,
    `runner=${attempt ? attempt.label : "unknown"}`,
    `exitCode=${exitCode}`,
    "",
    "[stdout]",
    stdout,
    "",
    "[stderr]",
    stderr,
  ].join("\n");

  fs.writeFileSync(commandLogFile, commandLog, "utf8");

  if (!fs.existsSync(resultsFile)) {
    writeFallbackResults(project, resultsFile, exitCode, stderr, runTimestamps);
  }

  return {
    project: project.name,
    cwd: project.cwd,
    startedAt,
    endedAt,
    exitCode,
    signal: result && result.signal ? result.signal : null,
    runner: attempt ? attempt.label : "unknown",
    commandLogPath: commandLogFile,
    reportPath: resultsFile,
    coveragePath: coverageDirectory,
    error: result && result.error ? result.error.message : null,
  };
}

function writeSummary(summary) {
  const summaryPath = path.join(reportsRoot, "summary.json");
  fs.writeFileSync(summaryPath, JSON.stringify(summary, null, 2), "utf8");
  return summaryPath;
}

function formatDurationMs(startIso, endIso) {
  const start = new Date(startIso).getTime();
  const end = new Date(endIso).getTime();
  if (!Number.isFinite(start) || !Number.isFinite(end) || end < start) {
    return "n/a";
  }

  const durationMs = end - start;
  return `${durationMs} ms`;
}

function writeMarkdownSummary(summary) {
  const summaryPath = path.join(reportsRoot, "summary.md");
  const header = "# Test Report Summary";
  const metadata = [
    `- Generated At: ${summary.generatedAt}`,
    `- Run Started At: ${summary.runStartedAt}`,
    `- Run Ended At: ${summary.runEndedAt}`,
    `- Duration: ${formatDurationMs(summary.runStartedAt, summary.runEndedAt)}`,
    `- Overall Status: **${summary.overallStatus.toUpperCase()}**`,
  ].join("\n");

  const tableHeader = [
    "| Project | Status | Exit Code | Runner | Report | Coverage |",
    "|---|---|---:|---|---|---|",
  ].join("\n");

  const rows = summary.results
    .map((result) => {
      const status = result.exitCode === 0 ? "PASSED" : "FAILED";
      return `| ${result.project} | ${status} | ${result.exitCode} | ${result.runner} | ${result.reportPath} | ${result.coveragePath} |`;
    })
    .join("\n");

  const content = [header, "", metadata, "", tableHeader, rows, ""].join("\n");
  fs.writeFileSync(summaryPath, content, "utf8");
  return summaryPath;
}

function getRequestedFormat() {
  const formatArgPrefix = "--format=";
  const formatArg = process.argv.find((arg) => arg.startsWith(formatArgPrefix));
  const rawFormat = formatArg ? formatArg.slice(formatArgPrefix.length) : "both";
  const normalized = rawFormat.toLowerCase();

  const validFormats = new Set(["json", "markdown", "both"]);
  if (!validFormats.has(normalized)) {
    console.error(`Invalid format \"${rawFormat}\". Use one of: json, markdown, both.`);
    process.exit(2);
  }

  return normalized;
}

function main() {
  ensureDirectory(reportsRoot);

  const requestedFormat = getRequestedFormat();

  const runStartedAt = new Date().toISOString();
  const results = projects.map(runProjectTests);
  const runEndedAt = new Date().toISOString();

  const hasFailures = results.some((result) => result.exitCode !== 0);

  const summary = {
    generatedAt: runEndedAt,
    runStartedAt,
    runEndedAt,
    overallStatus: hasFailures ? "failed" : "passed",
    results,
  };

  const generatedSummaries = [];
  if (requestedFormat === "json" || requestedFormat === "both") {
    generatedSummaries.push(writeSummary(summary));
  }
  if (requestedFormat === "markdown" || requestedFormat === "both") {
    generatedSummaries.push(writeMarkdownSummary(summary));
  }

  console.log("\nTest report summaries saved to:");
  generatedSummaries.forEach((summaryPath) => {
    console.log(`- ${summaryPath}`);
  });
  results.forEach((result) => {
    console.log(
      `- ${result.project}: exit ${result.exitCode} | report ${result.reportPath} | coverage ${result.coveragePath}`
    );
  });

  process.exit(hasFailures ? 1 : 0);
}

main();
